void StartDefaultTask(void const * argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();
  /* USER CODE BEGIN 5 */
  /* init tcp echo server */
  tcpecho_init();
  telnet_init();


  for(;;)
  {
    while (uart_rx_read_pointer != uart_rx_writ_pointer)
    {
      uint8_t received_byte = uart_rx_buffer[uart_rx_read_pointer];
      if (++uart_rx_read_pointer >= RX_BUFFER_LENGTH)
      {
        uart_rx_read_pointer = 0;
      }
      uart_byte_handler(received_byte);
    }

    osDelay(1);
  }
  /* USER CODE END 5 */
}

