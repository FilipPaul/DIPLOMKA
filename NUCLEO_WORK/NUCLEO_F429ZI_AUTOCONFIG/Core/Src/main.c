static void uart_command_handler(char* command){
  char * token = strtok(command, " ");
  if(strcasecmp(token,"info") == 0){
    printf("firware version: %s\n",version);
    printf("Available commands:\n");
    printf("IP address: %s\n", ip4addr_ntoa(&gnetif.ip_addr));
    printf("Subnet: %s\n", ip4addr_ntoa(&gnetif.netmask));
    printf("telnet - at PORT 23\n");
    printf("tcpecho - at PORT 7\n");
  }

  else if(strcasecmp(token,"MEASURE") == 0){
    if(token != NULL){
      token = strtok(NULL, " ");
      if(strcasecmp(token,"MATRIX") == 0){
        printf("[");
        uint16_t net = 0;
        for (uint32_t i = 0; i < sizeof(matrix_of_ports)/sizeof(matrix_of_ports[0]); i++)
        {
          if (HAL_GPIO_ReadPin(matrix_of_ports[i], matrix_of_pins[i]) == GPIO_PIN_SET)
          {
            (i== sizeof(matrix_of_ports)/sizeof(matrix_of_ports[0])) ? printf("-1]\n") : printf("-1,");
          }
          else
          {
            (i == sizeof(matrix_of_ports)/sizeof(matrix_of_ports[0])) ? printf("%u]\n",net++) : printf("%u,",net++);
          }
        }
        
      }
    }
  }


  else{
    printf("Unknown command: %s\n", token);
  }
}

static void uart_byte_handler(char received_byte){
  static uint16_t data_pointer;
  static char data_buffer[RX_BUFFER_LENGTH];  
  if(data_pointer < RX_BUFFER_LENGTH && received_byte >= 32 && received_byte <= 126){
    // received_byte >= 32 && received_byte <= 126 - printable ASCII chars
    data_buffer[data_pointer++] = received_byte;
  }

  if ((received_byte == '\n' || received_byte == '\r') && data_pointer > 0){
    data_buffer[data_pointer] = '\0'; //make c string from bytearray
    data_pointer = 0;
    uart_command_handler(data_buffer);
  }

}

